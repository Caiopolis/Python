
from math import pow,sqrt
print('Olá, aqui vamos realizar o calculo da hipotenusa 😎')
x = float(input('Digite o valor do cateto adjacente: '))
x2 = float(input('Agora digite o valor do cateto oposto: '))
hipo = pow(x,2) + pow(x,2)


print('Seu cateto adjacente é igual a {} e seu cateto oposto é {} \nLogo sua hipotenusa é {}'.format(x,x2,sqrt(hipo)))
